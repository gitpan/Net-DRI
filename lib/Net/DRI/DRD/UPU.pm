sub new
{
 my $class=shift;
 my $self=$class->SUPER::new(@_);
 return $self;
}

sub periods       { return map { DateTime::Duration->new(years => $_) } (1..10); }
sub name          { return 'UPU'; }
sub tlds          { return (qw/post edu.post org.post com.post/, map { lc($_).'.post' } keys %Net::DRI::Util::CCA2); }
sub object_types  { return qw/domain ns contact/; }
sub profile_types { return qw/epp whois/; }

sub transport_protocol_default
{
 my ($self,$type)=@_;

 return ('Net::DRI::Transport::Socket',{},'Net::DRI::Protocol::EPP',{})                                            if $type eq 'epp';
## return ('Net::DRI::Transport::Socket',{remote_host=>'whois.dotpostregistry.net '},'Net::DRI::Protocol::Whois',{}) if $type eq 'whois';
 return;
}

####################################################################################################

sub verify_name_domain
{
 my ($self,$ndr,$domain,$op)=@_;
 return $self->_verify_name_rules($domain,$op,{check_name => 1,
                                               my_tld => 1,
                                               icann_reserved => 1,
                                              });
}

